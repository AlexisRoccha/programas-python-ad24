while True:
    base = float(input("\nHola, para calcular el área del triangulo ocupo que me des la base: "))
    altura = float(input("Hola, para calcular el área del triangulo ocupo que me des la altura: "))

    area = (base * altura) / 2
    print(f"Para un triangulo de base {base}, y altura {altura}, el area es: {area}")

